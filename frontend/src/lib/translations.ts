export type TranslationKey = keyof typeof translations;

// Helper function to get translation
export const t = (key: TranslationKey, lang: 'en' | 'ta' = 'en'): string => {
  const translation = translations[key];
  if (typeof translation === 'object' && 'en' in translation) {
    return translation[lang] || translation.en;
  }
  return key;
};

// Get crop translation
export const getCropTranslation = (cropKey: string, lang: 'en' | 'ta' = 'en'): string => {
  const crop = translations.crops[cropKey as keyof typeof translations.crops];
  return crop ? crop[lang] : cropKey;
};
